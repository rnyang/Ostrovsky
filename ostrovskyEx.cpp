#include<vector>

void consumerUtility(int consumer, vector<int> contracts)
{
  int consumption = 0;
  for (vector<int>::iterator contract_it = contracts.begin(); contract_it != contracts.end(); ++contract_it)
    {
      if (contractDB[*it].target == consumer)
	consumption += contractDB[*it].qty;
    }
}
